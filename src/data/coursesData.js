// Función helper para obtener cursos destacados
export function getFeaturedCourses(limit = 3) {
  return coursesData.slice(0, limit);
}

// Función helper para obtener un curso por ID
export function getCourseById(id) {
  return coursesData.find((course) => course.id === id);
}

// Función helper para filtrar cursos
export function filterCourses({ search = "", type = "all", level = "all" }) {
  return coursesData.filter((course) => {
    const matchesSearch =
      course.title.toLowerCase().includes(search.toLowerCase()) ||
      course.description.toLowerCase().includes(search.toLowerCase());

    const matchesType =
      type === "all" ||
      (type === "live" && course.isLive) ||
      (type === "recorded" && !course.isLive);

    const matchesLevel =
      level === "all" || course.level.toLowerCase() === level.toLowerCase();

    return matchesSearch && matchesType && matchesLevel;
  });
}
